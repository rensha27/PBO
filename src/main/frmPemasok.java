    private void cmdSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cmdSimpanMouseClicked
        // TODO add your handling code here:
        String tKode=txtKode.getText();
        String tNama=txtNama.getText();
        String tAlamat=txtAlamat.getText();
        String tNoTelp=txtNoTelp.getText();
        String tEmail=txtEmail.getText();
        
        try{
           if (edit==true){
               stm.executeUpdate("update pemasok set nm_pems='"+tNama+"',alm_pems='"+tAlamat+"',phone='"+tNoTelp+"',email='"+tEmail+"' where kd_pems='" + tKode + "'");
            }else{
            stm.executeUpdate("INSERT into pemasok VALUES('"+tKode+"','"+tNama+"','"+tAlamat+"','"+tNoTelp+"','"+tEmail+"')");
            }
        tblPms.setModel(new DefaultTableModel(dataTable,header));
        baca_data();
        aktif(false);
        setTombol(true);
        }catch(SQLException e) {
        JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_cmdSimpanMouseClicked

