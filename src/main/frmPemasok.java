     private void baca_data(){
        try{
            stm = Con.createStatement();
            pstat = Con.prepareStatement("select * from pemasok",ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            RsPms = pstat.executeQuery();
            ResultSetMetaData meta = RsPms.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0;
        while(RsPms.next()){
            baris = RsPms.getRow();
        }
        dataTable = new Object[baris][col];
        int x = 0;
        RsPms.beforeFirst();
        while(RsPms.next()) {
            dataTable[x][0] = RsPms.getString("kd_pems");
            dataTable[x][1] = RsPms.getString("nm_pems");
            dataTable[x][2] = RsPms.getString("alm_pems");
            dataTable[x][3] = RsPms.getString("phone");
            dataTable[x][4] = RsPms.getString("email");
            x++;
        }
            tblPms.setModel(new DefaultTableModel(dataTable,header));
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
     
