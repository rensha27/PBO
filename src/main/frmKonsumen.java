    private void baca_data(){
        try{
            stm = Con.createStatement();
            pstat = Con.prepareStatement("select * from konsumen",ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            RsKsn = pstat.executeQuery();
            ResultSetMetaData meta = RsKsn.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0;
        while(RsKsn.next()){
            baris = RsKsn.getRow();
        }
        dataTable = new Object[baris][col];
        int x = 0;
        RsKsn.beforeFirst();
        while(RsKsn.next()) {
            dataTable[x][0] = RsKsn.getString("kd_kons");
            dataTable[x][1] = RsKsn.getString("nm_kons");
            dataTable[x][2] = RsKsn.getString("alm_kons");
            dataTable[x][3] = RsKsn.getString("kota_kons");
            dataTable[x][4] = RsKsn.getString("kd_pos");
            dataTable[x][5] = RsKsn.getString("phone");
            dataTable[x][6] = RsKsn.getString("email");
            x++;
        }
            tblKsn.setModel(new DefaultTableModel(dataTable,header));
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
